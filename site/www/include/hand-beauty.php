        <li><a href="#">La French manucure</a></li>
      </ul>
      <br/>
      <p>Pour chacune des deux prestations, 3 forfaits sont possibles :</p>
      <ul class="list-3">
        <li><a href="#"></a>Simple : en plus de la pose de vernis ou de la French, un petit modelage (petit massage des mains) est réalisé</li>
        <li><a href="#"></a>Coocooning : en plus de la pose de vernis ou de la French, un gommage et un modelage sont réalisés</li>
        <li><a href="#"></a>Prestige : en plus de la pose de vernis ou de la French, un gommage, un masque et un modelage sont réalisés</li>
      </ul>
    </div>
