  <h2 class="pad-1">La pose de vernis</h2>
  <div class="wrapper">
    <div class="extra-wrap">
      <p>La pose de vernis se décline en 2 types de poses :</p>
      <ul class="list-3">
        <li><a href="#">La pose de vernis simple</a> : durée de 5 jours.</li>
        <li><a href="#">La pose de vernis permanent</a> : durée de 15 jours.</li>
      </ul>
      <br/>
      <p>Plus de 60 couleurs sont à votre dispositions !</p>
    </div>
  </div>

  <br/>

  <h2 class="pad-1">La French manucure</h2>
  <div class="wrapper">
    <div class="extra-wrap">
      <p class="color-8">Sur ongles naturels au vernis simple</p>
      <br/>
      <p>La French Manucure sur ongles naturel requiert un minimum d’ongle naturel. Vos ongles sont limés à la forme et longueur souhaitées, puis le sourire est dessiné au vernis avant d’être recouvert d’un &laquo;top coat&raquo; (vernis de finition).</p>
    </div>
  </div>
  <br/>
  <br/>
  <div class="wrapper">
    <div class="extra-wrap">
      <p class="color-8">Sur ongles naturels au vernis permanent</p>
      <br/>
      <p>La French Manucure sur ongles naturel en gel est une prestation qui remplace la french au vernis simple et ayant pour avantage une meilleure tenue car le gel ne s’écaille pas aussi facilement que le vernis simple et ne part pas au dissolvant.</p>
      <p>Vos ongles sont limés à la forme et longueur souhaitées puis un gel blanc ainsi qu’un gel de finition sont posés sur une base adhérente simple.</p>
      <p>Vos ongles doivent avoir un minimum de longueur pour cette prestation, car ils ne seront en aucun cas rallongés sauf si on effectue une pose de gel.</p>
    </div>
