  <h3 class="pad-1">La prothesie ongulaire</h3>
  <h2 class="pad-1">La pose de faux ongles en gel</h2>
  <div class="wrapper">
    <div class="extra-wrap">
      <p>
        La pose de faux ongles en gel permet de mettre tous les ongles à la forme et à la longueur souhaitées, et adhère sur une durée de 3 semaines minimum sans problèmes.
        Le gel donne un effet bombé à l’ongle et n’empêche pas ceux-ci de respirer ni de pousser. Vous pouvez donc vous faire poser des ongles sans avoir à les laisser reposer.
      </p>
      <br/>
      <p>Un travail en 3 étapes :</p>
      <ul class="list-3">
        <li><a href="#"></a>Préparation de l'ongle</li>
        <li><a href="#"></a>Construction en gel</li>
        <li><a href="#"></a>Finition</li>
      </ul>
      <br/>
      <p>Au bout de 3 semaines, la pousse de l'ongle crée un décalage, il est donc conseillé de venir faire un «remplissage » afin de garder des ongles impeccables toute l’année.</p>
      <br/>
      <p>Avec cette prestation, deux possibilités s’offre à vous :</p>
      <ul class="list-3">
        <li><a href="#"></a>La French manucure</li>
        <li><a href="#"></a>La pose du gel au naturel</li>
      </ul>
    </div>
  </div>

  <br/>
  <br/>

  <div class="wrapper">
    <div class="extra-wrap">
      <p class="color-8">La French Manucure</p>
      <br/>
      <p>La French Manucure concerne le bout de l’ongle (ongle mort) peint en blanc. On peut procéder de deux manières pour effectuer cette prestation :</p>
      <ul class="list-3">
        <li><a href="#"></a>Avec du gel blanc ou du vernis blanc directement mis sur l’ongle</li>
        <li><a href="#"></a>Avec des capsules blanches (tips)</li>
      </ul>
      <p>Cette dernière méthode est la plus récente, elle apporte le résultat le plus parfait en finesse comme en régularité. Plusieurs formes deviennent alors possibles.</p>
    </div>
  </div>

  <br/>
  <br/>

  <div class="wrapper">
    <div class="extra-wrap">
      <p class="color-8">La pose du gel au naturel</p>
      <br/>
      <p>La pose du gel sans French, avec généralement un vernis à ongles en finition qui ne s’écaillera pas, car il n’est pas en contact direct avec l’ongle, et qui durera par conséquent plus longtemps. On peut terminer avec un gel de finition qui donnera un effet brillant.</p>
      <br/>
      <p>Lors du remplissage, les ongles sont raccourcis à la longueur souhaitée, le gel est alors poncé et la pousse de votre ongle préparée afin de reposer du gel sur l’intégralité de l’ongle. La pose de vernis comprend toujours une couche de base, deux couches de vernis puis une couche de «top coat» c'est-à-dire une base séchante, brillante et protectrice pour votre vernis.</p>
      <br/>
      <p>Les décorations sont évidemment toujours possibles...</p>
    </div>
  </div>

