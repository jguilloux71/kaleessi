  <h2 class="pad-1">La depose</h2>
  <div class="wrapper">
    <div class="extra-wrap">
      <p>
      La dépose d'ongles s’effectue à la ponceuse, ou avec un produit spécifique. Le travail est fait en douceur afin d’éviter d’abimer votre ongle naturel. Un soin est prévu à la fin de la prestation.</p>
    </div>
